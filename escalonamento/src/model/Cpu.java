package model;

public class Cpu {
	
	private int id;
	private String status;
	private String img;
	private int width;
	private int height;
	private String status_i;
	private String status_p;
	private String status_f;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public String getStatus_i() {
		return status_i;
	}
	public void setStatus_i(String status_i) {
		this.status_i = status_i;
	}
	public String getStatus_p() {
		return status_p;
	}
	public void setStatus_p(String status_p) {
		this.status_p = status_p;
	}
	public String getStatus_f() {
		return status_f;
	}
	public void setStatus_f(String status_f) {
		this.status_f = status_f;
	}
	
	
}
